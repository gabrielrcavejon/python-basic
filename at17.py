# Leia quatro números e calcule a média aritmética e imprima o resultado.

num1 = float(input(f"Digite o numero 1: "))
num2 = float(input(f"Digite o numero 2: "))
num3 = float(input(f"Digite o numero 3: "))
num4 = float(input(f"Digite o numero 4: "))

print(f"Media aritmetica dos 4 numeros: {(num1 + num2 + num3 + num4) / 4}")
