# 16. Faça a leitura de três valores e apresente como resultado a soma dos quadrados dos
# três números lidos.

num1 = float(input(f"Digite o numero 1: "))
num2 = float(input(f"Digite o numero 2: "))
num3 = float(input(f"Digite o numero 3: "))

print(f"oma dos quadrados dos valores: {num1 * num1 + num2 * num2 + num3 * num3}")
